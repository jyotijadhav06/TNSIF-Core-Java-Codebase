package com.day1;

public class PrimitiveDatabyte {
   public static void main(String[] args) {
	   //byte
	   byte byteMax=127;
	   byte byteMin=-128;
		
	   System.out.println("Min range of byte= "+byteMax+ "\nMax range of byte= "+byteMin);
   
	   //short
	   short shortMax=32767;
	   short shortMin=-32768;
		
	   System.out.println("Min range of short= "+shortMax+ "\nMax range of short= "+shortMin);
       
	   int intMax=2147483647;
	   int intMin=2147483647;
		
	   System.out.println("Min range of int= "+intMax+ "\nMax range of int= "+intMin);
   
   }
}
